						);


$db->query("CREATE TABLE Locations (
							LocationsID INT(5) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
							City VARCHAR(250) NULL DEFAULT NULL,
							ProvincesID INT(11)  NOT NULL,
							Phone VARCHAR(50) NULL DEFAULT NULL,
