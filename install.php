<?php

require_once "./config.php";


$db = new Purolator\Database();

$db->query("CREATE TABLE Admin (
							AdminID INT(5) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
							Username VARCHAR(150) NOT NULL,
