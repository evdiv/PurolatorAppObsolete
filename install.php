						);


$db->query("CREATE TABLE Provinces (
							ProvincesID INT(5) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
							ProvinceName VARCHAR(100) NULL DEFAULT NULL,
							ProvinceCode VARCHAR(2) NULL DEFAULT NULL)"
						);
